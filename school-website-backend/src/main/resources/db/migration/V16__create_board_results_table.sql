CREATE TABLE board_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    class_level VARCHAR(50) NOT NULL,
    assessment_year INTEGER NOT NULL,
    registered_students INTEGER NOT NULL,
    passed_students INTEGER NOT NULL,
    pass_percentage DOUBLE PRECISION NOT NULL,
    remarks VARCHAR(512),
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_board_results_tenant FOREIGN KEY (tenant_id) REFERENCES tenants (id) ON DELETE CASCADE
);
